--DROP TABLE TABELA_ESTADO;
CREATE TABLE TABELA_ESTADO
(
	ESTADO   CHAR(2)     NOT NULL,
	NOME     VARCHAR(50) NOT NULL
);
ALTER TABLE TABELA_ESTADO ADD CONSTRAINT ESTADO_PK PRIMARY KEY(ESTADO);

--DROP TABLE TABELA_CIDADE;
CREATE TABLE TABELA_CIDADE
(
	CODIGO_CIDADE INTEGER     NOT NULL,
	NOME_CIDADE   VARCHAR(50) NOT NULL,
	ESTADO        CHAR(2)     NOT NULL
);
ALTER TABLE TABELA_CIDADE ADD CONSTRAINT CIDADE_PK PRIMARY KEY (CODIGO_CIDADE);
ALTER TABLE TABELA_CIDADE ADD CONSTRAINT CIDADEUF_FK FOREIGN KEY (ESTADO) REFERENCES TABELA_ESTADO(ESTADO);

--DROP TABLE TABELA_EDIFICIO;
CREATE TABLE TABELA_EDIFICIO
(
	CODIGO_EDIFICIO INTEGER     NOT NULL,
	NOME_EDIFICIO   VARCHAR(50) NOT NULL,
	ANDARES         INTEGER     NOT NULL,
	CODIGO_CIDADE   INTEGER     NOT NULL,
  APTOPORANDAR    INTEGER     NOT NULL
);
ALTER TABLE TABELA_EDIFICIO ADD CONSTRAINT EDIFICIO_PK PRIMARY KEY (CODIGO_EDIFICIO);
ALTER TABLE TABELA_EDIFICIO ADD CONSTRAINT EDIFICIOCID_FK FOREIGN KEY (CODIGO_CIDADE) REFERENCES TABELA_CIDADE(CODIGO_CIDADE);

--DROP TABLE TABELA_APARTAMENTO;
CREATE TABLE TABELA_APARTAMENTO
(
	CODIGO_APARTAMENTO INTEGER       NOT NULL,
	CODIGO_EDIFICIO    INTEGER       NOT NULL,
	METRAGEM           NUMERIC(10,2) NOT NULL DEFAULT 0,
	ANDAR              INTEGER       NOT NULL,
	NUMERO_QUARTOS     INTEGER       NOT NULL
);
ALTER TABLE TABELA_APARTAMENTO ADD CONSTRAINT APARTAMENTO_PK PRIMARY KEY (CODIGO_APARTAMENTO);
ALTER TABLE TABELA_APARTAMENTO ADD CONSTRAINT APARTAMENTOEDIF_FK FOREIGN KEY (CODIGO_EDIFICIO) REFERENCES TABELA_EDIFICIO(CODIGO_EDIFICIO);

--DROP TABLE TABELA_PAGAMENTOS_CONDOMINIO;
CREATE TABLE TABELA_PAGAMENTOS_CONDOMINIO
(
	CODIGO_PAGAMENTO   INTEGER       NOT NULL,
	CODIGO_APARTAMENTO INTEGER       NOT NULL,
	DATA_PAGAMENTO     DATETIME      NOT NULL,
	VALOR_PAGAMENTO    NUMERIC(10,2) NOT NULL DEFAULT 0
);
ALTER TABLE TABELA_PAGAMENTOS_CONDOMINIO ADD CONSTRAINT PAGCOND_PK PRIMARY KEY (CODIGO_PAGAMENTO);
ALTER TABLE TABELA_PAGAMENTOS_CONDOMINIO ADD CONSTRAINT PAGCONDAPTO_FK FOREIGN KEY (CODIGO_APARTAMENTO) REFERENCES TABELA_APARTAMENTO(CODIGO_APARTAMENTO);

