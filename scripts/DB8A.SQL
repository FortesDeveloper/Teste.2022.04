SELECT E.*,
       C.*
  FROM TABELA_EDIFICIO E
       INNER JOIN TABELA_CIDADE C ON(C.CODIGO_CIDADE = E.CODIGO_CIDADE)
       INNER JOIN TABELA_APARTAMENTO A ON(A.CODIGO_EDIFICIO = E.CODIGO_EDIFICIO)
 WHERE C.ESTADO = 'SP'
   AND A.METRAGEM >= 100
   AND A.NUMERO_QUARTOS >= 2
   AND (E.ANDARES * E.APTOPORANDAR) = 30